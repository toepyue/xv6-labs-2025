// 在 syscall 函数最上面声明一个从用户空间拉取字符串参数的内核辅助函数
extern int argstr(int, char*, int);

void
syscall(void)
{
  int num;
  struct proc *p = myproc();

  num = p->trapframe->a7;
  if(num > 0 && num < NELEM(syscalls) && syscalls[num]) {
    // ⚠️ 1. 首先检查当前请求的系统调用是不是在被封锁的黑名单里
    if((1 << num) & p->syscall_mask) {
      // 🌟 2. 豁免检查：如果被封锁的是 open 或 exec，先去看看它的路径合不合法！
      if(num == SYS_open || num == SYS_exec) {
        char path[MAXPATH];
        // 对于 open(path, ...) 和 exec(path, ...)，要操作的路径永远在【第 0 个参数】！
        // 我们从用户空间把第 0 个字符串拉取到局部的 path 里进行对比：
        if(argstr(0, path, MAXPATH) >= 0) {
          // 利用系统的 strncmp 比较：如果当前试图访问的路径和白名单【完全相等】
          if(strncmp(path, p->allowed_path, MAXPATH) == 0) {
            // 破例放行！不执行下面的拦截拦截逻辑，直接执行对应内核调用！
            p->trapframe->a0 = syscalls[num]();
            return;
          }
        }
      }
      
      // ❌ 如果既没通过上面的豁免检查，又在黑名单里，直接无情拒之门外！
      p->trapframe->a0 = -1;
      return;
    }

    // 正常放行
    p->trapframe->a0 = syscalls[num]();
  } else {
    printf("%d %s: unknown sys call %d\n",
            p->pid, p->name, num);
    p->trapframe->a0 = -1;
  }
}
